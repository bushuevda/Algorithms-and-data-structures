func TestLineArray(t *testing.T) {
	for i, array := range input_data {
		x, y := DDA_line(array[0], array[1], array[2], array[3])
		if len(x) == len(outputX[i]) && len(x) == len(outputY[i]) && len(x) == len(y) {
			for j := 0; j < len(x); j++ {
				if !EqualsWithInacсuracy(x[j], outputX[i][j]) {
					t.Errorf("Некорректное значение в массиве X, получено: %d, должно быть: %d. Позиция: %d", int64(outputX[i][j]*MVALUE), int64(x[j]*MVALUE), i)
				} else if !EqualsWithInacсuracy(y[j], outputY[i][j]) {
					t.Errorf("Некорректное значение в массиве Y, получено: %d, должно быть: %d. Позиция: %d", int64(outputY[i][j]*MVALUE), int64(y[j]*MVALUE), i)
				}
			}
		}

	}
}
